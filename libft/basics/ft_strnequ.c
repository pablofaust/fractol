#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	int	x;

	if (s1 && s2)
	{
		x = ft_strncmp(s1, s2, n);
		if (x == 0)
			return (1);
	}
	return (0);
}
